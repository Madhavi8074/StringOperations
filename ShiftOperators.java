public class ShiftOperators {
    public static void main(String[] args){
        int num1=5;
        int leftShift=num1<<1;
        System.out.println("Left Shift (5<<1): "+leftShift);
        int num2=5;
        int rightShift=num2>>1;
        System.out.println("Right Shift (20>>2): "+rightShift);
    }
}
